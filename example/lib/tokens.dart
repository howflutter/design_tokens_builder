class SpecialColors extends ThemeExtension<SpecialColors> {
  SpecialColors({
    this.color1,
    this.color2,
  });

  final Color? color1;
  final Color? color2;

  @override
  SpecialColors copyWith({
    Color? color1,
    Color? color2,
  }) {
    return SpecialColors(
      color1: color1 ?? this.color1,
      color2: color2 ?? this.color2,
    );
  }

  @override
  SpecialColors lerp(SpecialColors? other, double t) {
    if (other is! SpecialColors) {
      return this;
    }
    return SpecialColors(
      color1: Color.lerp(color1, other.color1, t),
      color2: Color.lerp(color2, other.color2, t),
    );
  }
}

extension GeneratedTheme on ThemeData {
  SpecialColors? get specialColors => extension<SpecialColors>();
}

extension BuildContextExtension on BuildContext {
  ThemeData get theme => Theme.of(this);

  SpecialColors get specialColors => theme.extension<SpecialColors>()!;
  ColorScheme get colorScheme => theme.colorScheme;
  TextTheme get textTheme => theme.textTheme;
}
